    /**
     * Start element output
     */
    public function start_el(&$output, $item, $depth = 0, $args = null, $id = 0) {
        $indent = ($depth) ? str_repeat("\t", $depth) : '';
        
        $classes = empty($item->classes) ? array() : (array) $item->classes;
        $classes[] = 'menu-item-' . $item->ID;
        
        // Add custom classes for styling
        $classes[] = 'nav-item';
        if ($depth === 0) {
            $classes[] = 'nav-item-top';
        }
        
        $class_names = join(' ', apply_filters('nav_menu_css_class', array_filter($classes), $item, $args, $depth));
        $class_names = $class_names ? ' class="' . esc_attr($class_names) . '"' : '';
        
        $id_attr = apply_filters('nav_menu_item_id', 'menu-item-' . $item->ID, $item, $args, $depth);
        $id_attr = $id_attr ? ' id="' . esc_attr($id_attr) . '"' : '';
        
        $output .= $indent . '<li' . $id_attr . $class_names . '>';
        
        $atts = array();
        $atts['title']  = !empty($item->attr_title) ? $item->attr_title : '';
        $atts['target'] = !empty($item->target) ? $item->target : '';
        $atts['rel']    = !empty($item->xfn) ? $item->xfn : '';
        $atts['href']   = !empty($item->url) ? $item->url : '';
        $atts['class']  = 'nav-link group flex items-center gap-2 transition-all duration-300 hover:text-irimas-red';
        
        $atts = apply_filters('nav_menu_link_attributes', $atts, $item, $args, $depth);
        
        $attributes = '';
        foreach ($atts as $attr => $value) {
            if (!empty($value)) {
                $value = ('href' === $attr) ? esc_url($value) : esc_attr($value);
                $attributes .= ' ' . $attr . '="' . $value . '"';
            }
        }
        
        // Get icon based on menu item title or slug
        $icon_key = $this->get_icon_key($item);
        $icon = isset($this->menu_icons[$icon_key]) ? $this->menu_icons[$icon_key] : $this->menu_icons['default'];
        $icon_color = isset($this->icon_colors[$icon_key]) ? $this->icon_colors[$icon_key] : $this->icon_colors['default'];
        
        $item_output = $args->before;
        $item_output .= '<a' . $attributes . '>';
        $item_output .= '<span class="nav-icon ' . $icon_color . ' group-hover:text-irimas-red transition-colors">' . $icon . '</span>';
        $item_output .= $args->link_before . apply_filters('the_title', $item->title, $item->ID) . $args->link_after;
        $item_output .= '</a>';
        $item_output .= $args->after;
        
        $output .= apply_filters('walker_nav_menu_start_el', $item_output, $item, $depth, $args);
    }
    
    /**
     * Get icon key based on menu item
     */
    private function get_icon_key($item) {
        $title = strtolower($item->title);
        $url = strtolower($item->url);
        
        // Check title first
        foreach (array_keys($this->menu_icons) as $key) {
            if (strpos($title, $key) !== false) {
                return $key;
            }
        }
        
        // Check URL
        foreach (array_keys($this->menu_icons) as $key) {
            if (strpos($url, $key) !== false) {
                return $key;
            }
        }
        
        // Special cases
        if (strpos($title, 'order') !== false || strpos($url, 'order') !== false) {
            return 'order';
        }
        
        return 'default';
    }
}

